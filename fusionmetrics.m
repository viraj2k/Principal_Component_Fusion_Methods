function [ent qzy qhnc QWB]=fusionmetrics(A,B,F);
% clc;clear all;
% a1=imread('F:\matlab codes\CODES\MEDIAN FILTER ALG\LPCAFCM\med9\frmed91a1.bmp');
% h1=imread('F:\matlab codes\CODES\MEDIAN FILTER ALG\LPCAFCM\med9\frmed91h1.bmp');
% p1=imread('F:\matlab codes\CODES\MEDIAN FILTER ALG\LPCAFCM\med9\frmed91p1.bmp');
% lha=imread('F:\matlab codes\CODES\MEDIAN FILTER ALG\LPCAFCM\med9\frmed91lha16.bmp');
% lpa=imread('F:\matlab codes\CODES\MEDIAN FILTER ALG\LPCAFCM\med9\fmed91lpa8.bmp');
a1=A;
h1=B;
lha=F;
enta1=entropy(double(a1));
enth1=entropy(double(h1));
% enttp1=entropy(double(p1));
entlha=entropy(double(lha));
ent={enta1 enth1 entlha};
% entlpa=entropy(double(lpa));
milhaa1=mi(a1,lha);
milhah1=mi(h1,lha);
% mitlpap1=mi(p1,lpa);
% milpaa1=mi(a1,lpa);
qzyah=(milhaa1+milhah1)/2;
qzy={milhaa1 milhah1 qzyah};
% qzyap=(mitlpap1+milpaa1)/2;
qhnc1=milhaa1/(enta1+entlha);
qhnc2=milhah1/(enth1+entlha);
qhnc3=(qhnc1+qhnc2)/2;
qhnc={qhnc1 qhnc2 qhnc3};
sda1=std2(a1);
sdh1=std2(h1);
sdlha=std2(lha);
mea1=mean2(a1);
meh1=mean2(h1);
melha=mean2(lha);
covalha=corr2(double(a1(:)),double(lha(:)));
covhlha=corr2(double(h1(:)),double(lha(:)));
QWB1=((2*covalha)/(sda1^2+sdlha^2))*((2*mea1*melha)/(mea1^2+melha^2));
QWB2=((2*covhlha)/(sdh1^2+sdlha^2))*((2*meh1*melha)/(meh1^2+melha^2));
QWB3=(QWB1+QWB2)/2;
QWB={QWB1 QWB2 QWB3};
end

